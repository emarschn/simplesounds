import UIKit

extension UIColor {
    
    @nonobjc class var brightBlueColor: UIColor {
        return UIColor(red: 0.0, green: 122.0/255.0, blue: 255.0/255.0, alpha: 1.0)
    }
    
    @nonobjc class var deepRedColor: UIColor {
        return UIColor(red: 139.0/255.0, green: 0.0, blue: 0.0, alpha: 1.0)
    }
}
